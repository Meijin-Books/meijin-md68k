;-----------------------------------------------------
; 第6章　はじめてのプログラム
; 画面に HELLO と表示する
;-----------------------------------------------------

VDP_DATA    equ $C00000     ; VDPデータポート
VDP_CTRL    equ $C00004     ; VDPコントロールポート

RAM_START   equ $FF0000
STACK_TOP   equ $FFFFFE

;-----------------------------------------------------
; ベクタテーブル（$000000〜$0000FF）
;-----------------------------------------------------
    org     $000000

    dc.l    STACK_TOP       ; $000: 初期スタックポインタ
    dc.l    EntryPoint      ; $004: 初期PC（リセット後の飛び先）

    ; 残りの例外ベクタは、すべて何もしない場所へ
    dcb.l   62, Exception   ; $008〜$0FF

;-----------------------------------------------------
; ROMヘッダ（$000100〜$0001FF）
;-----------------------------------------------------
    org     $000100

    dc.b    "SEGA MEGA DRIVE "              ; コンソール名
    dc.b    "(C)MEIJIN 2026  "              ; 著作権表記
    dc.b    "HELLO                                           " ; 国内タイトル
    dc.b    "HELLO                                           " ; 海外タイトル
    dc.b    "GM 00000000-00"                ; シリアル番号
    dc.w    $0000                           ; チェックサム
    dc.b    "J               "              ; 対応デバイス（Jはパッド）
    dc.l    $00000000                       ; ROM開始位置
    dc.l    $000FFFFF                       ; ROM終了位置
    dc.l    $00FF0000                       ; RAM開始位置
    dc.l    $00FFFFFF                       ; RAM終了位置
    dc.b    "            "                  ; SRAM情報（なし）
    dc.b    "                                        " ; 備考
    dc.b    "JUE             "              ; 対応リージョン

;-----------------------------------------------------
; プログラム本体（$000200〜）
;-----------------------------------------------------
    org     $000200

EntryPoint:
    move.w  #$2700,sr           ; 割り込みを止める

    ; --- VDPレジスタの初期化 ---
    lea     VDP_CTRL,a0         ; A0 = コントロールポート
    lea     VDPRegs,a1          ; A1 = 設定値の並び
    move.w  #$8000,d0           ; D0 = レジスタ0への書き込み命令
    moveq   #23,d1              ; 24本ぶん（dbraなので23）

InitVDP:
    move.b  (a1)+,d0            ; 設定値を D0 の下位バイトへ
    move.w  d0,(a0)             ; コントロールポートへ送る
    add.w   #$0100,d0           ; 次のレジスタ番号へ
    dbra    d1,InitVDP

    ; --- パレットを CRAM に書く ---
    move.l  #$C0000000,VDP_CTRL ; 「CRAMの$0000番に書く」と告げる
    lea     Palette,a1
    moveq   #15,d1              ; 16色

CopyPal:
    move.w  (a1)+,VDP_DATA      ; データポートへ流し込む
    dbra    d1,CopyPal

    ; --- タイルデータを VRAM に書く ---
    move.l  #$40000000,VDP_CTRL ; 「VRAMの$0000番に書く」と告げる
    lea     Tiles,a1
    move.w  #(TilesEnd-Tiles)/2-1,d1

CopyTiles:
    move.w  (a1)+,VDP_DATA
    dbra    d1,CopyTiles

    ; --- ネームテーブルに文字を並べる ---
    ; スクロールAは $C000。11行目の12桁目から置く
    ; 1行は64マス×2バイト=128バイト
    ; アドレス = $C000 + (11 * 128) + (12 * 2) = $C598
    move.l  #$45980003,VDP_CTRL  ; 「VRAMの$C598番に書く」と告げる
    lea     Message,a1
    moveq   #4,d1               ; 5文字

CopyMap:
    move.w  (a1)+,VDP_DATA
    dbra    d1,CopyMap

    ; --- 画面を点ける ---
    move.w  #$8144,VDP_CTRL     ; レジスタ1：表示ON

Done:
    bra     Done                ; 意図的な無限ループ

Exception:
    rte

;-----------------------------------------------------
; データ
;-----------------------------------------------------
VDPRegs:
    dc.b    $04     ; 00: HBlank割り込み禁止
    dc.b    $04     ; 01: 表示OFF・VBlank割り込み禁止（後で点ける）
    dc.b    $30     ; 02: スクロールA のネームテーブル = $C000
    dc.b    $3C     ; 03: ウィンドウ = $F000
    dc.b    $07     ; 04: スクロールB のネームテーブル = $E000
    dc.b    $6C     ; 05: スプライトテーブル = $D800
    dc.b    $00     ; 06: 未使用
    dc.b    $00     ; 07: 背景色 = パレット0の色0
    dc.b    $00     ; 08: 未使用
    dc.b    $00     ; 09: 未使用
    dc.b    $FF     ; 10: HBlank割り込みの間隔
    dc.b    $00     ; 11: スクロール方式
    dc.b    $81     ; 12: 40桁モード
    dc.b    $37     ; 13: 水平スクロールテーブル = $DC00
    dc.b    $00     ; 14: 未使用
    dc.b    $02     ; 15: オートインクリメント = 2
    dc.b    $01     ; 16: スクロールサイズ 64x32
    dc.b    $00     ; 17: ウィンドウ水平位置
    dc.b    $00     ; 18: ウィンドウ垂直位置
    dc.b    $FF     ; 19: DMA長さ 下位
    dc.b    $FF     ; 20: DMA長さ 上位
    dc.b    $00     ; 21: DMA元アドレス 下位
    dc.b    $00     ; 22: DMA元アドレス 中位
    dc.b    $80     ; 23: DMA元アドレス 上位
    even

Palette:
    dc.w    $0000   ; 色0：透明（背景色）
    dc.w    $0EEE   ; 色1：白
    dc.w    $000E   ; 色2：赤
    dc.w    $0E00   ; 色3：青
    dc.w    $00E0   ; 色4：緑
    dc.w    $0000,$0000,$0000,$0000,$0000,$0000,$0000
    dc.w    $0000,$0000,$0000

Message:
    ; ネームテーブルの中身＝タイル番号。1がH、2がE…
    dc.w    1,2,3,3,4       ; H E L L O

Tiles:
    ; タイル0：空白（32バイト＝全部0）
    dcb.l   8,$00000000

    ; タイル1：H
    dc.l    $01100110
    dc.l    $01100110
    dc.l    $01100110
    dc.l    $01111110
    dc.l    $01100110
    dc.l    $01100110
    dc.l    $01100110
    dc.l    $00000000

    ; タイル2：E
    dc.l    $01111110
    dc.l    $01100000
    dc.l    $01100000
    dc.l    $01111100
    dc.l    $01100000
    dc.l    $01100000
    dc.l    $01111110
    dc.l    $00000000

    ; タイル3：L
    dc.l    $01100000
    dc.l    $01100000
    dc.l    $01100000
    dc.l    $01100000
    dc.l    $01100000
    dc.l    $01100000
    dc.l    $01111110
    dc.l    $00000000

    ; タイル4：O
    dc.l    $00111100
    dc.l    $01100110
    dc.l    $01100110
    dc.l    $01100110
    dc.l    $01100110
    dc.l    $01100110
    dc.l    $00111100
    dc.l    $00000000
TilesEnd:

    end
